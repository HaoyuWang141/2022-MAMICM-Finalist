function [v_r,P_r]=climb(a,h,i)
if i==1
    syms v;
    sol=solve((0.00458*574.75)./(263.25-1798.64).*v^4-(h*0.00458)./a.*v^3+(75*9.8.*a.*574.75)./(263.25-1798.64).*v^2+(574.75-75*9.8*h).*v+(263.25*h./a-263.25*574.75/(263.25-1798.64))==0,v,"Real",true);
    v=double(sol);
    P=75*9.8*v*a+0.00458*v.^3;
end

if i==2
    syms v;
    sol=solve((0.00367*328.35)/(353.7-1250.65).*v^4-(h*0.00367)/a.*v^3+(65*9.8*a*328.35)/(353.7-1250.65).*v^2+(328.35-65*9.8*h).*v+(353.70*h/a-353.70*328.35/(353.7-1250.65))==0,v,"Real",true);
    v=double(sol);
    P=65*9.8*v*a+0.00367*v.^3;
end

if i==3
    syms v;
    sol=solve((0.00328*302.45)/(224.95-1031.42)*v^4-(h*0.00328)/a.*v^3+(55*9.8*a*302.45)/(224.95-1031.42).*v^2+(302.45-55*9.8*h).*v+(224.95*h/a-224.95*302.45/(224.95-1031.42))==0,v,"Real",true);
    v=double(sol);
    P=55*9.8*v*a+0.00328*v.^3;
end

v_r=v(1)
